package studentdata;

public class student 
{
	int Id;
	String Name;
	String Subject;
	int CAmark;
	int ESEmark;
	int total;
	
	
	int getId()
	{
		return Id;
	}
	void setId(int Id)
	{
		this.Id=Id;
	}
	
	String getName()
	{
		return Name;
	}
	void setName(String Name)
	{
		this.Name=Name;
	}
	
	String getSubject()
	{
		return Subject;
	}
	void setSubject(String Subject)
	{
		this.Subject=Subject;
	}
	
	int getCAmark()
	{
		return CAmark;
	}
	void setCAmark(int CAmark)
	{
		this.CAmark=CAmark;
	}
	
	int getESEmark()
	{
		return ESEmark;
	}
	void setESEmark(int ESEmark)
	{
		this.ESEmark=ESEmark;
	}
	
	int gettotal()
	{
		return total;
	}
	void settotal(int total)
	{
		this.total=total;
	}

	
}
